            <p>El proyecto OMI ofrece un conjunto de herramientas y recursos que ayudan en el aprendizaje 
de la teoría de intérpretes y los lenguajes formales. OMI en si mismo es un intérprete desarrollado 
con la finalidad de servir como caso práctico, aplicando los conceptos estudiados en 
estos campos.</p> 

<p>OMI es un intérprete modular, en el sentido de que su funcionalidad y características puede ser extendida mediante módulos. </p>

<p>OMI es código abierto y libre, por lo que puede ser utilizado, estudiado, modificado y/o redistribuido libremente.</p>

<p>El nombre OMI también hace referencia al lenguaje que es interpretado. Un lenguaje de programación de alto nivel, de tipado dinámico y de propósito general. Fundamentalmente 
imperativo orientado a objetos, pero que además presenta características propias de otros paradigmas como de la programación funcional. </p>

<p>El proyecto OMI pretende facilitar el estudio de los sistemas intérpretes mediante la documentación y la interactividad. Abarcando no solo el diseño y el desarrollo
de uno de estos sistemas, si no el proceso de interpretación en si. De esta forma se presentan herramientas que permiten hacer uso del intérprete a la vez que ilustran
su funcionamiento.</p>

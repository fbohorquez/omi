               <h1 id="title-sec">Conclusiones</h1>
               En esta sección se detallan las lecciones aprendidas tras el desarrollo del proyecto OMI y
se identifican las posibles mejoras futuras sobre el desarrollo del software.
<H1><A NAME="SECTION00030000000000000000">
Objetivos alcanzados</A>
</H1>
Como se introdujo se ha construido un intérprete completo de un lenguaje de programación de 
alto nivel, capaz de describir mediante estructuras de datos el proceso de interpretación llevado a cabo. También se 
ha construido un cliente web capaz de interpretar estos datos y representarlos gráficamente y de forma interactiva.

<P>
Se recorrido todo el proceso de desarrollo y construcción del proyecto. La documentación generada ha sido dispuesta 
en una plataforma web que permite su acceso y navegación de una forma fácil y cómoda. 

<P>

<H1><A NAME="SECTION00040000000000000000">
Lecciones aprendidas</A>
</H1>

<UL>
<LI>Se ha profundizado en los estudios de los intérpretes de lenguajes de formales y los conceptos que los hacen posibles tales como 
los autómatas, el léxico, la sintaxis, las gramáticas, los autómatas, los árboles...
</LI>
<LI>Se han estudiado y asimilado características avanzadas de los lenguajes de programación y cómo estas se implementan.
</LI>
<LI>Se ha profundizado en otros lenguajes de programación para construir una visión amplia de características y paradigmas (PHP, Python, JavaScript, Java, Haskell, Ruby...). 
</LI>
<LI>Se han afianzados nuevos conocimientos en el desarrollo C++ y algunas características avanzadas de este, como la inclusión de bibliotecas dinámicas, bibliotecas 
de líneas de comandos o las directivas del compilador.
</LI>
<LI>Se ha estudiado y profundizado en herramientas de compilación automática y la paquetización de binarios precompilados.
</LI>
<LI>Se ha profundizado en la instalación y configuración de servidores.
</LI>
<LI>Se han estudiado estructuras de datos estándar utilizadas en comunicaciones como JSON.
</LI>
<LI>Se ha profundizado en el proceso de desarrollo de un proyecto software y las metodologías aplicadas.
</LI>
<LI>Se ha profundizado en el desarrollo de diagramas y el lenguaje UML.
</LI>
<LI>Se han estudiado estándares ISO para asegurar la calidad de proyectos.

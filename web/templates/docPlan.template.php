<H1><A NAME="SECTION00080000000000000000">
Aseguramiento de calidad</A>
</H1>

<P>
Para asegurar una calidad aceptable del producto y los procesos llevados a cabo en el desarrollo se ha tomado como esquema el conjunto de normas ISO 9000. 
El sistema de gestión de la calidad está formado por procesos y es en si mismo un proceso en el que ingresan los requisitos del sistema, y se obtiene un
producto que cumple los requisitos y satisface al cliente. 

<P>
A continuación se describen los procesos y actividades seguidas para asegurar la calidad.

<P>

<UL>
<LI>Se implementa un ciclo de vida evolutivo en el que se examina y documenta cada decisión tomada, al final de cada iteración se comprueba la calidad del producto obtenido.
</LI>
<LI>El equipo directivo se asegura de que los requisitos del cliente se determinan y cumplen. El objetivo de esto es aumentar la satisfacción del mismo.
</LI>
<LI>El equipo directivo realiza una planificación en la que se capturen las acciones a seguir para alcanzar los objetivos perseguidos.
</LI>
<LI>Los roles y responsabilidades del equipo quedan definido y atribuidos en cada iteración del ciclo.
</LI>
<LI>En cada iteración se examina las pruebas de calidad realizadas, la retroalimentación del cliente y la conformidad del producto.  
</LI>
<LI>En cada iteración se contemplan las mejoras del producto en relación con los requisitos y necesidades del cliente.
</LI>
<LI>Todo el personal tiene los conocimientos y el entrenamiento adecuados para realizar la tarea que en la planificación se le ha sido asignada.
</LI>
<LI>Se utilizan entrevistas periódicas con el cliente para capturar los requisitos necesarios en cada iteración. Además de validar la corrección del producto obtenido
</LI>
<LI>Para la toma de requisitos se ha utilizado técnicas de objetivos medibles, de forma que los requisitos impuesto por el cliente se ven como objetivos generales. 
Estos son analizados repetidamente para obtener los requisitos críticos para el funcionamiento del sistema. En cada iteración se refinan los requisitos generales y se plantean 
nuevos, aplicándose la misma técnica para determinar los tomados en la iteración. 
</LI>
<LI>Se presentan modelos de caso de uso que deben ser comprendidos y validados por el cliente. 
</LI>
<LI>El diseño del producto obtenido en cada iteración queda documentado mediante modelos.
</LI>
<LI>Se realiza una revisión, verificación y validación de los diseños propuestos.
</LI>
<LI>La actividad de desarrollo del producto quedará bien documentada en el propio código. 
</LI>
<LI>En cada iteración se definirán los casos de pruebas, estas serán llevadas a cabo para comprobar la corrección del producto.
</LI>
